/* Math */
const numero = 10.5;

/*
Ceil - Redonde para arriba el decimal
Floor - Devuelve el entero
Round - Redondea segun el decimal al entero que sigue o devuelve el actual 
*/

console.log(Math.ceil(numero));
console.log(Math.floor(numero));
console.log(Math.round(numero));

/* min - max */
console.log(Math.min(9, 8, 1, 5, 6));
console.log(Math.max(9, 8, 1, 5, 6));

/* random */
console.log(Math.random());
/* random entero */
console.log(Math.round(Math.random() * 25));
